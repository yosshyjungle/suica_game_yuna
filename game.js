// „Ç≤„Éº„É†„ÅÆÂü∫Êú¨Ë®≠ÂÆö
const GAME_CONFIG = {
    CANVAS_WIDTH: 400,
    CANVAS_HEIGHT: 600,
    GRAVITY: 0.5,
    FRICTION: 0.98,
    BOUNCE: 0.7,
    FRUIT_SIZE_MULTIPLIER: 1.2
};

// „Éï„É´„Éº„ÉÑ„ÅÆÁ®ÆÈ°û„Å®Ë®≠ÂÆö
const FRUIT_TYPES = [
    { name: 'cherry', emoji: 'üçí', size: 20, color: '#ff6b6b', points: 1 },
    { name: 'strawberry', emoji: 'üçì', size: 30, color: '#ff4757', points: 3 },
    { name: 'grape', emoji: 'üçá', size: 40, color: '#a55eea', points: 6 },
    { name: 'orange', emoji: 'üçä', size: 50, color: '#ffa502', points: 10 },
    { name: 'apple', emoji: 'üçé', size: 60, color: '#ff3838', points: 15 },
    { name: 'watermelon', emoji: 'üçâ', size: 80, color: '#2ed573', points: 25 }
];

// „Ç≤„Éº„É†Áä∂ÊÖãÁÆ°ÁêÜ
class GameState {
    constructor() {
        this.canvas = document.getElementById('gameCanvas');
        this.ctx = this.canvas.getContext('2d');
        this.nextFruitCanvas = document.getElementById('nextFruitCanvas');
        this.nextFruitCtx = this.nextFruitCanvas.getContext('2d');
        
        this.fruits = [];
        this.score = 0;
        this.gameOver = false;
        this.nextFruitType = 0;
        
        this.mouseX = GAME_CONFIG.CANVAS_WIDTH / 2;
        this.isMouseDown = false;
        
        this.setupEventListeners();
        this.generateNextFruit();
        this.gameLoop();
    }
    
    // „Ç§„Éô„É≥„Éà„É™„Çπ„Éä„Éº„ÅÆË®≠ÂÆö
    setupEventListeners() {
        // „Éû„Ç¶„Çπ„Ç§„Éô„É≥„Éà
        this.canvas.addEventListener('mousemove', (e) => {
            const rect = this.canvas.getBoundingClientRect();
            this.mouseX = e.clientX - rect.left;
        });
        
        this.canvas.addEventListener('mousedown', (e) => {
            if (!this.gameOver) {
                this.dropFruit();
            }
        });
        
        // „Çø„ÉÉ„ÉÅ„Ç§„Éô„É≥„ÉàÔºà„É¢„Éê„Ç§„É´ÂØæÂøúÔºâ
        this.canvas.addEventListener('touchmove', (e) => {
            e.preventDefault();
            const rect = this.canvas.getBoundingClientRect();
            const touch = e.touches[0];
            this.mouseX = touch.clientX - rect.left;
        });
        
        this.canvas.addEventListener('touchstart', (e) => {
            e.preventDefault();
            if (!this.gameOver) {
                this.dropFruit();
            }
        });
        
        // „É™„Çπ„Çø„Éº„Éà„Éú„Çø„É≥
        document.getElementById('restartButton').addEventListener('click', () => {
            this.restart();
        });
    }
    
    // „Éï„É´„Éº„ÉÑ„ÇíËêΩ‰∏ã„Åï„Åõ„Çã
    dropFruit() {
        const fruitType = FRUIT_TYPES[this.nextFruitType];
        const fruit = new Fruit(this.mouseX, 50, fruitType);
        this.fruits.push(fruit);
        this.generateNextFruit();
    }
    
    // Ê¨°„ÅÆ„Éï„É´„Éº„ÉÑ„ÇíÁîüÊàê
    generateNextFruit() {
        this.nextFruitType = Math.floor(Math.random() * 3); // ÊúÄÂàù„ÅÆ3Á®ÆÈ°û„Åã„Çâ„É©„É≥„ÉÄ„É†
        this.drawNextFruit();
    }
    
    // Ê¨°„ÅÆ„Éï„É´„Éº„ÉÑ„ÇíÊèèÁîª
    drawNextFruit() {
        const ctx = this.nextFruitCtx;
        const fruitType = FRUIT_TYPES[this.nextFruitType];
        
        ctx.clearRect(0, 0, 40, 40);
        ctx.fillStyle = fruitType.color;
        ctx.beginPath();
        ctx.arc(20, 20, fruitType.size / 2, 0, Math.PI * 2);
        ctx.fill();
        
        // „Éï„É´„Éº„ÉÑ„ÅÆÁµµÊñáÂ≠ó„ÇíÊèèÁîª
        ctx.font = '20px Arial';
        ctx.textAlign = 'center';
        ctx.fillText(fruitType.emoji, 20, 25);
    }
    
    // „Ç≤„Éº„É†„É´„Éº„Éó
    gameLoop() {
        this.update();
        this.draw();
        
        if (!this.gameOver) {
            requestAnimationFrame(() => this.gameLoop());
        }
    }
    
    // „Ç≤„Éº„É†Áä∂ÊÖã„ÅÆÊõ¥Êñ∞
    update() {
        // „Éï„É´„Éº„ÉÑ„ÅÆÁâ©ÁêÜÊºîÁÆó
        for (let i = this.fruits.length - 1; i >= 0; i--) {
            const fruit = this.fruits[i];
            fruit.update();
            
            // ÁîªÈù¢Â§ñ„Å´Âá∫„Åü„Éï„É´„Éº„ÉÑ„ÇíÂâäÈô§
            if (fruit.y > GAME_CONFIG.CANVAS_HEIGHT + 100) {
                this.fruits.splice(i, 1);
                continue;
            }
            
            // „Ç≤„Éº„É†„Ç™„Éº„Éê„ÉºÂà§ÂÆö
            if (fruit.y < 0) {
                this.gameOver = true;
                this.showGameOverScreen();
                return;
            }
            
            // ‰ªñ„ÅÆ„Éï„É´„Éº„ÉÑ„Å®„ÅÆË°ùÁ™ÅÂà§ÂÆö
            for (let j = i + 1; j < this.fruits.length; j++) {
                const otherFruit = this.fruits[j];
                if (this.checkCollision(fruit, otherFruit)) {
                    this.handleCollision(fruit, otherFruit, i, j);
                    break;
                }
            }
        }
    }
    
    // Ë°ùÁ™ÅÂà§ÂÆö
    checkCollision(fruit1, fruit2) {
        const dx = fruit1.x - fruit2.x;
        const dy = fruit1.y - fruit2.y;
        const distance = Math.sqrt(dx * dx + dy * dy);
        const minDistance = (fruit1.size + fruit2.size) / 2;
        
        return distance < minDistance;
    }
    
    // Ë°ùÁ™ÅÂá¶ÁêÜ
    handleCollision(fruit1, fruit2, index1, index2) {
        // Âêå„ÅòÁ®ÆÈ°û„ÅÆ„Éï„É´„Éº„ÉÑ„Åã„ÉÅ„Çß„ÉÉ„ÇØ
        if (fruit1.type.name === fruit2.type.name) {
            // Âêà‰ΩìÂá¶ÁêÜ
            const newFruitTypeIndex = FRUIT_TYPES.findIndex(f => f.name === fruit1.type.name) + 1;
            
            if (newFruitTypeIndex < FRUIT_TYPES.length) {
                // Êñ∞„Åó„ÅÑ„Éï„É´„Éº„ÉÑ„Çí‰ΩúÊàê
                const newFruitType = FRUIT_TYPES[newFruitTypeIndex];
                const newX = (fruit1.x + fruit2.x) / 2;
                const newY = (fruit1.y + fruit2.y) / 2;
                const newFruit = new Fruit(newX, newY, newFruitType);
                
                // „Çπ„Ç≥„Ç¢Âä†ÁÆó
                this.score += fruit1.type.points + fruit2.type.points;
                this.updateScore();
                
                // „Éï„É´„Éº„ÉÑ„ÇíÂâäÈô§„Åó„Å¶Êñ∞„Åó„ÅÑ„Éï„É´„Éº„ÉÑ„ÇíËøΩÂä†
                this.fruits.splice(index1, 1);
                this.fruits.splice(index2 - 1, 1); // index1„ÇíÂâäÈô§„Åó„Åü„ÅÆ„Åßindex2„ÇíË™øÊï¥
                this.fruits.push(newFruit);
                
                // „Ç¢„Éã„É°„Éº„Ç∑„Éß„É≥ÂäπÊûú
                this.addBounceEffect(newX, newY);
            }
        } else {
            // Áï∞„Å™„ÇãÁ®ÆÈ°û„ÅÆÂ†¥Âêà„ÅØÁâ©ÁêÜÁöÑ„Å™ÂèçÁô∫
            this.handleBounce(fruit1, fruit2);
        }
    }
    
    // Áâ©ÁêÜÁöÑ„Å™ÂèçÁô∫Âá¶ÁêÜ
    handleBounce(fruit1, fruit2) {
        const dx = fruit1.x - fruit2.x;
        const dy = fruit1.y - fruit2.y;
        const distance = Math.sqrt(dx * dx + dy * dy);
        
        if (distance < (fruit1.size + fruit2.size) / 2) {
            const overlap = (fruit1.size + fruit2.size) / 2 - distance;
            const separationX = (dx / distance) * overlap / 2;
            const separationY = (dy / distance) * overlap / 2;
            
            fruit1.x += separationX;
            fruit1.y += separationY;
            fruit2.x -= separationX;
            fruit2.y -= separationY;
            
            // ÈÄüÂ∫¶„ÅÆ‰∫§Êèõ
            const tempVx = fruit1.vx;
            const tempVy = fruit1.vy;
            fruit1.vx = fruit2.vx * GAME_CONFIG.BOUNCE;
            fruit1.vy = fruit2.vy * GAME_CONFIG.BOUNCE;
            fruit2.vx = tempVx * GAME_CONFIG.BOUNCE;
            fruit2.vy = tempVy * GAME_CONFIG.BOUNCE;
        }
    }
    
    // „Éê„Ç¶„É≥„ÇπÂäπÊûú„ÇíËøΩÂä†
    addBounceEffect(x, y) {
        // Á∞°Âçò„Å™Ë¶ñË¶öÂäπÊûúÔºàÂÆüÈöõ„ÅÆÂÆüË£Ö„Åß„ÅØ„Çà„ÇäË§áÈõë„Å™„Ç®„Éï„Çß„ÇØ„Éà„ÇíËøΩÂä†ÂèØËÉΩÔºâ
        console.log(`Âêà‰ΩìÔºÅ‰ΩçÁΩÆ: (${x}, ${y})`);
    }
    
    // ÊèèÁîªÂá¶ÁêÜ
    draw() {
        const ctx = this.ctx;
        
        // ËÉåÊôØ„Çí„ÇØ„É™„Ç¢
        ctx.clearRect(0, 0, GAME_CONFIG.CANVAS_WIDTH, GAME_CONFIG.CANVAS_HEIGHT);
        
        // ËÉåÊôØ„Ç∞„É©„Éá„Éº„Ç∑„Éß„É≥
        const gradient = ctx.createLinearGradient(0, 0, 0, GAME_CONFIG.CANVAS_HEIGHT);
        gradient.addColorStop(0, '#87CEEB');
        gradient.addColorStop(1, '#98FB98');
        ctx.fillStyle = gradient;
        ctx.fillRect(0, 0, GAME_CONFIG.CANVAS_WIDTH, GAME_CONFIG.CANVAS_HEIGHT);
        
        // „Éï„É´„Éº„ÉÑ„ÇíÊèèÁîª
        this.fruits.forEach(fruit => fruit.draw(ctx));
        
        // ËêΩ‰∏ã‰∫àÂÆö‰ΩçÁΩÆ„ÅÆË°®Á§∫
        if (!this.gameOver) {
            const fruitType = FRUIT_TYPES[this.nextFruitType];
            ctx.fillStyle = 'rgba(255, 255, 255, 0.5)';
            ctx.beginPath();
            ctx.arc(this.mouseX, 50, fruitType.size / 2, 0, Math.PI * 2);
            ctx.fill();
            
            ctx.strokeStyle = 'rgba(0, 0, 0, 0.3)';
            ctx.lineWidth = 2;
            ctx.beginPath();
            ctx.arc(this.mouseX, 50, fruitType.size / 2, 0, Math.PI * 2);
            ctx.stroke();
        }
    }
    
    // „Çπ„Ç≥„Ç¢Êõ¥Êñ∞
    updateScore() {
        document.getElementById('score').textContent = this.score;
    }
    
    // „Ç≤„Éº„É†„Ç™„Éº„Éê„ÉºÁîªÈù¢Ë°®Á§∫
    showGameOverScreen() {
        document.getElementById('finalScore').textContent = this.score;
        document.getElementById('gameOverScreen').classList.remove('hidden');
    }
    
    // „Ç≤„Éº„É†ÂÜçÈñã
    restart() {
        this.fruits = [];
        this.score = 0;
        this.gameOver = false;
        this.updateScore();
        document.getElementById('gameOverScreen').classList.add('hidden');
        this.generateNextFruit();
        this.gameLoop();
    }
}

// „Éï„É´„Éº„ÉÑ„ÇØ„É©„Çπ
class Fruit {
    constructor(x, y, type) {
        this.x = x;
        this.y = y;
        this.vx = 0;
        this.vy = 0;
        this.type = type;
        this.size = type.size;
        this.rotation = 0;
    }
    
    // „Éï„É´„Éº„ÉÑ„ÅÆÊõ¥Êñ∞
    update() {
        // ÈáçÂäõ„ÇíÈÅ©Áî®
        this.vy += GAME_CONFIG.GRAVITY;
        
        // ‰ΩçÁΩÆ„ÇíÊõ¥Êñ∞
        this.x += this.vx;
        this.y += this.vy;
        
        // Êë©Êì¶„ÇíÈÅ©Áî®
        this.vx *= GAME_CONFIG.FRICTION;
        
        // Â£Å„Å®„ÅÆË°ùÁ™ÅÂà§ÂÆö
        if (this.x - this.size / 2 < 0) {
            this.x = this.size / 2;
            this.vx *= -GAME_CONFIG.BOUNCE;
        }
        if (this.x + this.size / 2 > GAME_CONFIG.CANVAS_WIDTH) {
            this.x = GAME_CONFIG.CANVAS_WIDTH - this.size / 2;
            this.vx *= -GAME_CONFIG.BOUNCE;
        }
        
        // Â∫ïÈù¢„Å®„ÅÆË°ùÁ™ÅÂà§ÂÆö
        if (this.y + this.size / 2 > GAME_CONFIG.CANVAS_HEIGHT) {
            this.y = GAME_CONFIG.CANVAS_HEIGHT - this.size / 2;
            this.vy *= -GAME_CONFIG.BOUNCE;
            this.vx *= GAME_CONFIG.FRICTION;
        }
        
        // ÂõûËª¢
        this.rotation += 0.1;
    }
    
    // „Éï„É´„Éº„ÉÑ„ÅÆÊèèÁîª
    draw(ctx) {
        ctx.save();
        ctx.translate(this.x, this.y);
        ctx.rotate(this.rotation);
        
        // „Éï„É´„Éº„ÉÑ„ÅÆÂΩ±
        ctx.fillStyle = 'rgba(0, 0, 0, 0.2)';
        ctx.beginPath();
        ctx.ellipse(2, this.size / 2 + 2, this.size / 2, this.size / 4, 0, 0, Math.PI * 2);
        ctx.fill();
        
        // „Éï„É´„Éº„ÉÑÊú¨‰Ωì
        ctx.fillStyle = this.type.color;
        ctx.beginPath();
        ctx.arc(0, 0, this.size / 2, 0, Math.PI * 2);
        ctx.fill();
        
        // „Éï„É´„Éº„ÉÑ„ÅÆ„Éè„Ç§„É©„Ç§„Éà
        ctx.fillStyle = 'rgba(255, 255, 255, 0.3)';
        ctx.beginPath();
        ctx.arc(-this.size / 4, -this.size / 4, this.size / 6, 0, Math.PI * 2);
        ctx.fill();
        
        // „Éï„É´„Éº„ÉÑ„ÅÆÁµµÊñáÂ≠ó
        ctx.font = `${this.size}px Arial`;
        ctx.textAlign = 'center';
        ctx.textBaseline = 'middle';
        ctx.fillText(this.type.emoji, 0, 0);
        
        ctx.restore();
    }
}

